    /**
     * Show the form for editing the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function edit($id)
    {
        $updateAdmin = Admin::find($id);
        return view('admin.edit', compact(
            "updateAdmin"
        ));
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $updateAdmin = Admin::find($id);
        $updateAdmin->name = $request->name;
        $updateAdmin->email = $request->email;
        $updateAdmin->password = $request->password;
        $updateAdmin->role = $request->role;
        $updateAdmin->save();

        return redirect('admin');
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $delete = Admin::find($id);
        $delete->delete();

        return redirect('admin');
    }
